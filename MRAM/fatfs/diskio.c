#include "ff.h"			/* Obtains integer types */
#include "diskio.h"		/* Declarations of disk functions */
#include "mram.h"

/* Definitions of physical drive number for each drive */
#define DEV_MRAM		0


/*-----------------------------------------------------------------------*/
/* Get Drive Status                                                      */
/*-----------------------------------------------------------------------*/

DSTATUS disk_status (
	BYTE pdrv		/* Physical drive nmuber to identify the drive */
)
{
	DSTATUS stat;
	int result;


	if (pdrv == DEV_MRAM){
		// get MRAM status
		return RES_OK;
	} else{
		return STA_NOINIT;		
	}
}



/*-----------------------------------------------------------------------*/
/* Inidialize a Drive                                                    */
/*-----------------------------------------------------------------------*/

DSTATUS disk_initialize (
	BYTE pdrv				/* Physical drive nmuber to identify the drive */
)
{
	DSTATUS stat;
	int result;

	if (pdrv == DEV_MRAM){
		// initialize MRAM
		return RES_OK;
	} else{
		return STA_NOINIT;		
	}
}



